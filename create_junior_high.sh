# 定义创建文件的函数
create_school_file() {
    local city="$1"
    local district="$2"
    local school_name="$3"
    local filename="${city}-${district}-${school_name}.md"
    local filepath="${BASE_DIR}/${filename}"

    cat > "$filepath" << EOF
